                            <h3>Submission Preparation Checklist</h3>
<p>As part of the submission process, authors are required to check off their submission's compliance with all of the following items, and submissions may be returned to authors that do not adhere to these guidelines.</p>
<p>•   The submission has not been previously published, nor is it before another journal for consideration (or an explanation has been provided in Comments to the Editor).</p>
<p>•   The submission file is in OpenOffice, Microsoft Word, or RTF document file format.</p>
<p>•   Where available, URLs for the references have been provided.</p>
<p>•   The text is single-spaced; uses a 12-point font; employs italics, rather than underlining (except with URL addresses); and all illustrations, figures, and tables are placed within the text at the appropriate points, rather than at the end.</p>
<p>•  The text adheres to the stylistic and bibliographic requirements outlined in the Author Guidelines.</p>
<h3>Author Guidelines</h3>
Manuscripts, including the abstract, should be typed double spaced on one side only of A-4 paper and should not exceed 20 pages. ( Times New Roman, Font Size 12)
<p>1.  Each article should include an abstract of between 150 and 200 words which succinctly presents the content of the paper.</p>
<p>2.  The contribution submitted to ISEL should not have been published previously and should not be under consideration elsewhere.</p>
<p>3.  A hard copy of the paper should be submitted (where possible) with an e-mail attachment of the paper to the Editor, ISEL, Department of English, Obafemi Awolowo University, Ile-Ife, Nigeria.</p>
<p>4.  Submissions should be made by e-mail to info@dapafued.com</p>
<p>5.  Submissions should be accompanied by a short biography, stating qualifications, areas of interest/specialization, institutional affiliation, etc</p>
